class Solution(object):
    def minimumBribes(self, arr):
        too_chaotic = False
        num_of_bribes = 0
        for x in range(
            len(arr)
        ):  # first traverse the array to check too chaotic condition
            if arr[x] - x > 3:
                too_chaotic = True
                print("Too chaotic")
                return

        for x in range(
            len(arr) - 3, -1, -1
        ):  # Traverse array in reverse fashion(starting from n-3) and compare if its curr is greater than its right two elements, if yes simply increment the bribr
            for j in range(x, x + 2):
                if arr[j] > arr[j + 1]:
                    temp = arr[j]
                    arr[j] = arr[j + 1]
                    arr[j + 1] = temp
                    num_of_bribes = num_of_bribes + 1
        print(num_of_bribes)


