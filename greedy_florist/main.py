from audioop import reverse
from importlib.metadata import distribution


class Solution(object):
    def getMinimumCost(self, k, c):
        min_cost = 0 
        c.sort()
    ## buy most expensive one
        distribution = len(c) / k
        iter = len(c) - 1
        i = 0
        while i < distribution:
            for j in range (iter, iter - k, -1):
                if j < 0:
                    break
                current_price = (i + 1) * c[j]
                min_cost = min_cost + current_price
            iter = j - 1
            i = i + 1
        return min_cost


